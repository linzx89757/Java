package domain;

public class Classes {
	private String classes_id;
	private String classes_name;
	private String classes_speciality;
	private int classes_teacher;
	private int grade;
	public String getClasses_id() {
		return classes_id;
	}
	public void setClasses_id(String classes_id) {
		this.classes_id = classes_id;
	}
	public String getClasses_name() {
		return classes_name;
	}
	public void setClasses_name(String classes_name) {
		this.classes_name = classes_name;
	}
	public String getClasses_speciality() {
		return classes_speciality;
	}
	public void setClasses_speciality(String classes_speciality) {
		this.classes_speciality = classes_speciality;
	}
	public int getClasses_teacher() {
		return classes_teacher;
	}
	public void setClasses_teacher(int classes_teacher) {
		this.classes_teacher = classes_teacher;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
}
